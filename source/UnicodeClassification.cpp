struct ClosedRange {
	uint32_t first, last;
};

static
bool
LessThan (
	const ClosedRange & a,
	const ClosedRange & b
) {
	return a.last < b.first;
}

static inline
bool
Contains (
	const ClosedRange & a,
	uint32_t character
) {
	return a.first <= character && character <= a.last;
}

static inline
bool
Contains (
	const ClosedRange * begin,
	const ClosedRange * end,
	uint32_t character
) {
	const ClosedRange one = { character, character };
	const ClosedRange * p(std::lower_bound(begin, end, one, LessThan));
	if (p >= end) return false;
	return Contains(*p, character);
}

